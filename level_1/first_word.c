#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;

	if (argc > 1)
	{
		i = 0;
		while (*argv[1] <= 32)
			argv[1]++;
		while (argv[1][i] > 32)
			i++;
		if (i > 1)
			write(1, argv[1], i);
	}
	write(1, "\n", 1);
	return (0);
}
