#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	char	*s;

	i = 0;
	s = argv[1];
	while (s[i] == 32)
		s++;
	while (s[i] != 32 && s[i] != '\n' && s[i] != '\0')
		i++;
	if (i > 1)
		write(1, s, i);
	write(1, "\n", 1);
	return (0);
}
