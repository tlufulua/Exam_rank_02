#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	char	*s;

	if (argc > 1)
	{
		i = 0;
		s = argv[1];
		while (s[i] == 32)
			s++;
		while (s[i] != 32 && s[i] != 10 && s[i] != 0 && (s[i] < 48 || s[i] > 57))
			i++;
		if (i > 1)
			write(1, s, i);
	}
	write(1, "\n", 1);
	return (0);
}
