#include <unistd.h>

int	main(int argc, char **argv)
{
	while (argc == 2 && *argv[1])
	{
		if ((*argv[1] >= 65 && *argv[1] <= 89) || \
		(*argv[1] >= 97 && *argv[1] <= 121))
			*argv[1] += 1;
		else if (*argv[1] == 90 || *argv[1] == 122)
			*argv[1] -= 25;
		write(1, argv[1]++, 1);
	}
	write(1, "\n", 1);
	return (0);
}
