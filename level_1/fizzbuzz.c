#include <unistd.h>

int	main(void)
{
	int		x;
	char	c;

	x = 1;
	while (x <= 100)
	{
		if (!(x % 3))
			write(1, "fizz", 4);
		if (!(x % 5))
			write(1, "buzz", 4);
		if ((x % 3) && (x % 5))
		{
			if (x >= 10)
			{
				c = (x / 10) + '0';
				write(1, &c, 1);
			}
			c = (x % 10) + '0';
			write(1, &c, 1);
		}
		write(1, "\n", 1);
		x++;
	}
	return (0);
}
